#include "libft.h"

int		ft_isspace(int c)
{
	if ((c == '\t') || (c == '\n') || (c == ' '))
		return (1);
	else if ((c == '\r') || (c == '\f') || (c == '\v'))
		return (1);
	else
		return (0);
}
